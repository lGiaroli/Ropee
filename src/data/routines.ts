import { defaultStrengthExercises } from '@/data/strength';
import { estimateRoutineDuration } from '@/features/workouts/routineBuilder';
import { RoutinePhaseTemplate, WorkoutRoutine } from '@/types/domain';

const now = new Date().toISOString();

type RoutineInput = Omit<WorkoutRoutine, 'createdAt' | 'estimatedDuration' | 'estimatedJumpDuration' | 'strengthExercises' | 'updatedAt'> & {
  customPhases?: RoutinePhaseTemplate[];
  strengthIds?: string[];
};

const strengthFor = (ids: string[] = []) =>
  defaultStrengthExercises.map((exercise) => ({
    ...exercise,
    enabled: ids.includes(exercise.id),
  }));

const withEstimates = (input: RoutineInput): WorkoutRoutine => {
  const routine = {
    ...input,
    createdAt: now,
    strengthExercises: strengthFor(input.strengthIds),
    updatedAt: now,
  };
  const estimate = estimateRoutineDuration(routine);
  return {
    ...routine,
    estimatedDuration: estimate.totalDuration,
    estimatedJumpDuration: estimate.jumpDuration,
  };
};

const phase = (
  type: RoutinePhaseTemplate['type'],
  durationSeconds: number,
  label: string,
  message?: string,
): RoutinePhaseTemplate => ({
  type,
  durationSeconds,
  label,
  message,
});

